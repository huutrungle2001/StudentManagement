    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try{
            if (txtStudentId.getText().equals("")){
                 MessageDialogHelper.showMessageDialog(parentForm, "Mã sinh viên cần phải nhập", "Lỗi");
                return;
            }
            BangDiemLogin login = new BangDiemLogin();
            BangDiem bd = login.findByMaSinhVien(txtStudentIdSearch.getText());
            
            if (bd == null){
                MessageDialogHelper.showMessageDialog(parentForm, "Mã sinh viên không tồn tại trong bảng điểm", "Lỗi");
                return;
            }
            if (MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn xóa điểm của sinh viên không?", "Hỏi")
                    == JOptionPane.NO_OPTION){
                return;
            }
            if (login.deleteByMaSinhVien(txtStudentId.getText())){
                MessageDialogHelper.showMessageDialog(parentForm, 
                        "Điểm của sinh viên đã được xóa", "Thông báo");
                loadDataToTable();
            }
            else {
                MessageDialogHelper.showMessageDialog(parentForm, 
                        "Điểm của sinh viên không thể xóa được", "Thông báo");
        } 
    }catch (Exception e){
        e.printStackTrace();
        MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

