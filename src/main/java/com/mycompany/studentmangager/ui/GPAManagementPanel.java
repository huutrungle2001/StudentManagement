    private void loadDataToTable(){
        try{
            BangDiemLogin login = new BangDiemLogin();
            List<BangDiem> list = login.findAll();
            //xóa dữ liệu đang hiển thị trên bảng
            tblModel.setRowCount(0);
            for (BangDiem bd : list){
                tblModel.addRow(new Object[]{
                    bd.getMaSinhVien(), bd.getTiengNhat(), bd.getOOP(), bd.getGDTC(),
                    (bd.getTiengNhat() + bd.getGDTC() + bd.getOOP()) /3
            });
                
            }
            
        // cập nhật lại dữ liệu trong bảng
        tblModel.fireTableDataChanged();
        }catch(Exception e){
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(this, e.getMessage(), "Lỗi");
        }
           
    }
